  <h1>ABC123</h1>
  <hr>
  <p>One course we took called "Introduction to Digital Media" was designed to introduce us to the development of web-based and mobile digital media. It was a hands on course where we worked in groups to deliver projects in a similar manner to that in a professional environment. It taught technical skills together with how to use a creative approach to design and a focus on user and business needs.</p>
  <p>One piece of coursework that we were set was to create a mobile application using current web technologies such as HTML5, CSS3 and JavaScript. We had a number of options to choose from, but we opted to create an educational app for children which we targeted at young children aged 3-5 years of age. As this is often around the time that children begin learning to read and count we wanted to design something that would help facilitate this. Each section of our app was split into two parts, one part for learning the material and one part in the form of quiz for user to test themselves on the material.</p>
