    <p>The aim of the game is to survive oncoming waves of different enemies all of whom are intent on destroying the player's ship and attempting to achieve a high score.</p> 
    <p>The player controls the ship's movement by tilting the phone and can fire at enemies by repeatedly tapping on the screen. The player must also collect the red fuel canisters to avoid running out of fuel, which would prevent the player from moving.</p>
    <p>You can view more details and get the application by clicking on the link below. Alternatively, search for "Void Attack" in the Play Store from your Android smartphone from your mobile.</p>
