  <h1>Void Attack</h1>
  <hr>
  <p>"Introduction to Programming" was first year course is designed to teach the fundamental principles of programming a computer. As part of this course we used a programming language called Processing that is designed to help develop highly interactive rich media applications from the very beginning.</p>
  <p>Each week we were set the task of creating a drawing, animation or game using all of the principles that we had learned in the lectures thusfar.</p>
  <p>At the end of the course we were asked to bring together everything that we had learned over the year to create a "mini project" using the Processing language. I decided to create a game for Android smartphones which I named Void Attack.</p>
