  <p>At the start of 2014 I enrolled onto a course called <a href="https://www.edx.org/course/harvardx/harvardx-cs50x-introduction-computer-1022" target="_blank">CS50x An Introduction to Computer Science</a> which was offered through the online learning initiative <a href="https://www.edx.org/" target="_blank">edX</a>. I was looking for a really thorough introduction to the discipline which couldn't be had through traditional coding courses and this course really fit the bill. As part of the course, we were required to complete various programming exercises some of which are shown below.</p>
